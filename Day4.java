public class Day4 {
    public static void main(String[] args) {
        // 1. \n = baris baru
        System.out.println("contoh baris pertama\ncontoh baris kedua");

        // 2. \t = tab
        System.out.println("nama:\tzahra");
        System.out.println("umur:\t19");

        // 3. \\ = backslash
        System.out.println("folder: C:\\Users\\zahra");

        // 4. \" = tanda kutip ganda
        System.out.println("teman bilang: \"ayo belajar java bareng\"");

        // 5. \' = tanda kutip tunggal
        System.out.println("karakter ini: \'Z\'");

        // 6. \r = carriage return
        System.out.println("halo\rzahaa");

        // 7. \b = backspace
        System.out.println("helloo\b!");

        // 8. \f = form feed
        System.out.println("ini sebelum\fini sesudah");

        // 9. \u2605 = simbol bintang (â˜…)
        System.out.println("simbol bintang: \u2605");
    }
}
