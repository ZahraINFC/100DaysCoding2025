public class print {
    public static void main(String[] args) {
        System.out.print("halo ");
        System.out.print("zahra");
    }
}

public class printf {
    public static void main(String[] args) {
        String nama = "zahra";
        int umur = 19;
        double ipk = 6.00;

        System.out.printf("nama: %s, umur: %d, ipk: %.2f", nama, umur, ipk);
    }
}

